            marker.setMap(map);
            marker.setPosition(place.geometry.location);
            $('.g-latitude').val(place.geometry.location.lat());
            $('.g-longitude').val(place.geometry.location.lng());
            $(".g-addressDetails").val(place.formatted_address);
        }

